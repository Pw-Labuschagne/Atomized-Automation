Map::Map()
{
	Material = Texture_Manager::Load_Texture("Graphical_Evidence/Material.png");
	Water = Texture_Manager::Load_Texture("Graphical_Evidence/Water.png");
	Ground = Texture_Manager::Load_Texture("Graphical_Evidence/Ground.png");

	Load_Map(Map_Lvl);

	SRC.x = SRC.y = 0;
	SRC.h = DEST.h = 32;
	SRC.w = DEST.w = 32;

	DEST.x = DEST.y = 0;
}


/**/
void Map::Load_Map(int arr[25][25])
{
	for(int r = 0; r < 25; r++)
	{
		for(int c = 0; c < 25; c++)
		{
			Map_Arr[r][c] = arr[r][c];
		}
	}
}

/**/
void Map::Draw_Map()
{
	int Background = 0;

	for(int r = 0; r < 25; r++)
        {
                for(int c = 0; c < 25; c++)
                {
			Background = Map_Arr[r][c];
			
			DEST.x = c * 32;
			DEST.y = r * 32;

			switch (Background){
				case 0:
					Texture_Manager::Draw_Map(Water, SRC, DEST);
					break;
				case 1:
					Texture_Manager::Draw_Map(Ground, SRC, DEST);
					break;
				case 2:
					Texture_Manager::Draw_Map(Material, SRC, DEST);
					break;
				default:
					break;
			}
                }
        }

}
